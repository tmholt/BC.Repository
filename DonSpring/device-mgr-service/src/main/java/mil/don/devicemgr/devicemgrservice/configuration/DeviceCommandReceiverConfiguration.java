package mil.don.devicemgr.devicemgrservice.configuration;

import org.springframework.amqp.core.Binding;
import org.springframework.amqp.core.BindingBuilder;
import org.springframework.amqp.core.Exchange;
import org.springframework.amqp.core.FanoutExchange;
import org.springframework.amqp.core.Queue;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;

import java.util.Arrays;
import java.util.List;
import java.util.UUID;

@Configuration
public class DeviceCommandReceiverConfiguration
{
    @Bean
    public Exchange commandsExchange() {
        return new FanoutExchange("device-commands");
    }


    // this is our personal queue of rabbitmq device commands
    @Bean
    public Queue deviceCommandsQueue() {
        return new Queue(
            "device-commands-queue",
            false, true, true);
    }


    @Bean
    public Binding binding() {

        Binding binding1 = BindingBuilder.bind(
            deviceCommandsQueue())
            .to(commandsExchange())
            .with("device.*")
            .noargs();


        return binding1;
    }
}
